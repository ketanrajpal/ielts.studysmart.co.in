		<ol type="1">
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Speaking Test - Part 1</strong>
				<ol type="a">
					<li>
						Make a list of all possible topics you could be interviewed on:
						<ul>
							<li>Your home</li>
							<li>Your country</li>
							<li>Your job</li>
							<li>Your interests</li>
							<li>Films</li>
							<li>Books</li>
							<li>Festivals</li>
							<li>Food</li>
							<li>Clothes</li>
							<li>Hobbies etc..</li>
						</ul>
					</li>
					<li>
						Record yourself: Record yourself and see how you may improve yourself. Self evaluation may help you a lot!
					</li>
					<li>
						Vocabulary: Make sure that you prepare yourself well in advance for all the possible vocabulary. Practice the pronunciation of any new vocabulary.
					</li>
					<li>
						Think about the language that will be useful. For example:
						<ul>
							<li>Ways of expressing likes and dislikes</li>
							<li>Linkers like ‘even though’ and ‘unless’</li>
							<li>Tenses to talk about the past and present and your experiences</li>
						</ul>
					</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Speaking Test - Part 2</strong>
				<ol type="a">
					<li>Read the topic card carefully: Make sure that you have understood what you need to talk about.</li>
					<li>Jot down a few points: Pen down a few points to answer the prompts on the topic card. Don’t waste time in writing sentences.</li>
					<li>Answer both parts of the topic Card: Make sure you answer the description as well as the explanation part of the topic card.</li>
					<li>Use examples from your own life: Make sure that you use examples from your own life. It’s much easier to talk about your own experiences fluently.</li>
					<li>Linking Words usage: Try to expand your sentences using linking words like however, although etc and avoid usage of moreover and thus which are normally used for writing.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For For IELTS Speaking Test - Part 3</strong>
				<ol type="a">
					<li>Read English Language: Make it a habit to read English language newspapers and watch the news reports to keep yourself updated on everyday topics</li>
					<li>
						Think about the language: Ponder upon the language which will be useful example.
						<ul>
							<li>Language of comparison and contrast</li>
							<li>Language to express opinions and give reasons</li>
							<li>Language to speculate about the future</li>
							<li>Language to discuss hypothetical example conditional sentences</li>
						</ul>
					</li>
					<li>Take a  current affairs issue every day and list all the vocabulary: Try to take a current affairs issue every day and list all the vocabulary you would need to talk about it. Think about your opinion on this issue and think about opposing views as well.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips Before The For IELTS Speaking Test </strong>
				<ol type="a">
					<li>Try and talk in English as much as possible.</li>
					<li>Remember the more familiar you are with everyday social English the more fluent you will sound.</li>
					<li>Make sure you are familiar with the structure of the test.</li>
					<li>Talk aloud to yourself and tape yourself.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips During The For IELTS Speaking Test</strong>
				<ol type="a">
					<li>Your appearance may have an unconscious effect on the examiner, so dress neatly.</li>
					<li>Arrive early. Sometimes examiners get ahead of their schedule.</li>
					<li>Try to look confident. Don’t fold your arms.</li>
					<li>Keep eye contact with the examiner, especially in parts 1 and 3. Don’t speak to the cassette recorder</li>
					<li>Don’t worry if the examiner does not look at you in part 2. He or she will be listening and checking the criteria for assessing you.</li>
					<li>Don’t worry about being nervous. Everyone is nervous doing these tests. Don’t say to the examiner ‘I’m nervous!</li>
					<li>Remember speaking ‘fluently’, does not mean speaking ‘quickly’.</li>
					<li>Don’t be afraid to correct yourself if you make a grammatical mistake.</li>
				</ol>
			</li>
		</ol>
	</div>
