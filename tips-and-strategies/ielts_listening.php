			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For  IELTS Listening Questions - Labelling A Diagram Which Has Labelled Part</strong>
				<ol class="mt-1" type="a">
					<li>Read the instructions carefully: They will state how many words you should use if there is not a box of labels provided. You could be asked to write one word only.</li>
					<li>Study the diagram: You may be able to predict one or two answers or associated vocabulary that you may hear connected to a particular label.</li>
					<li>Check whether the labels are numbered in a clockwise or anti - clockwise direction: The information will be given in the same order as the numbers on the diagram.Write down or select the labels as you listen. Use the words you hear, not your own.</li>
					<li>Listen carefully when words are spelt out: Write the words even if you don’t know where they go.</li>
					<li>Use the information: Use the information given on the diagram to help you if you get lost.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips For IELTS Listening Test:</strong>
				<ol class="mt-1" type="a">
					<li>
						Before the exam
						<ul>
							<li>Make sure you are familiar with the instructions for the different question types so you can quickly glance at the questions and know what to do.</li>
							<li>Think about the type of situations you might come across living or studying in an English speaking country, such as someone asking for directions or talks on topics of general interest.</li>
							<li>Listen to as wide a range of material as possible – English language television and radio, listening sites on the Internet and films and music are all great sources.</li>
							<li>Do as many practice tests as you can to get used to the speed of delivery and the task types.</li>
							<li>
								Focus on the strategies and skills you need for listening. For example:
								<ul>
									<li>‘listening for specific information’</li>
									<li>‘guessing meaning of words’</li>
									<li>‘recognising signposts’</li>
									<li>‘understanding lectures & talks’</li>
								</ul>
							</li>
						</ul>
					</li>
					<li>
						During the exam
						<ol class="mt-1" type="a">
							<li>
								Before listening
								<ul>
									<li>Make sure you use the 30 seconds given at the beginning of each section to read the questions before listening.</li>
									<li>Make sure you look at all the questions in each section.</li>
									<li>Make sure you read the instructions carefully.</li>
									<li>Make sure you know what form your answer should take e.g. a word, a letter, a tick, a phrase, a circle around the correct letter etc</li>
									<li>Make sure you use the time given to analyze the questions.</li>
									<li>Decide what you are listening for. Are you listening for specific information, for the main idea or for the speaker’s attitude?</li>
									<li>Use the outline given of the topic, who is talking and the situation to</li>
									<li>Predict possible answers.</li>
									<li>When you look at the questions, think about synonyms. You may  not hear the exact words that are in the answer choices.</li>
								</ul>
							</li>
							<li>
								While listening
								<ul>
									<li>Listen carefully to the instructions.</li>
									<li>Listen out for key words and common signpost words like – ‘on the other hand’, ‘in contrast’ etc</li>
									<li>Remember the questions usually follow the same sequence as the conversation or talk but the information may be expressed differently from the questions.</li>
									<li>Write down something even if you haven’t got the answer before moving on to the next question.</li>
									<li>Make sure you continue to listen while you are writing as you only hear the tape once.</li>
									<li>Don’t get stuck on a question you can’t answer. Just move on.</li>
									<li>Remember there may be sections of irrelevant information. Just keep listening for the information you need.</li>
									<li>Write notes in the question booklet as you listen. This may help you go back to a difficult question later.</li>
								</ul>
							</li>
							<li>
								After listening
								<ul>
									<li>Make sure you transfer your answers correctly.</li>
									<li>Make sure you use the time given to check your answers.</li>
									<li>If you can’t remember an answer, guess.</li>
								</ul>
							</li>
						</ol>
					</li>
