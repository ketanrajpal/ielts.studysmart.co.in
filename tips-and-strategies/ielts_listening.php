				<strong>Preparation Strategy For IELTS Listening Questions –notes / Table / Summary / Form / Flowchart / Diagram </strong>
				<ol class="mt-1" type="a">
					<li>
						Read the sentences carefully: Look at the table or form etc carefully before the recording starts and during the 30 seconds given. As you do this,
						<ul>
							<li>Examine any headings or subheadings.</li>
							<li>Try to work out what you are going to hear.</li>
							<li>Decide what you need to listen out for.</li>
							<li>Anticipate grammatical form as well as vocabulary.</li>
							<li>If a box of answers is given, see if you can guess any of the matches & eliminate unlikely answers.</li>
						</ul>
					</li>
					<li>
						Answer the questions as you listen: Answer the questions as you listen. The answers will be given in the same order as the questions although they may be expressed differently.
					</li>
					<li>Remember to make notes: Make notes beside the question if you are not sure of the answer.</li>
					<li>Number of words: The answer could be one word, two words or three words but not four  or more. If you think you need more than three words your answer is probably incorrect.</li>
					<li>If there is a box of answers, there will be at least two you don’t need.</li>
					<li>If there is no box, the answer could be one word, two words or three words but not four or more.</li>
					<li>If you think you need more than three words your answer is probably incorrect.</li>
					<li>There may be alternative rubrics for these completion questions e.g. ‘complete the notice’ or ‘complete the explanation’ or ‘complete the news report’.</li>
				</ol>
