				<strong>Preparation Strategy For IELTS Listening Questions - Multiple Choice </strong>
				<ol class="mt-1" type="a">
					<li>
						Skim the questions and the answer choices quickly: Skim the IELTS Listening questions and the answer choices quickly before the recording starts. As you do this,
						<ul>
							<li>Underline the key words – the words that give you the most information, such as the ‘wh-’ words and nouns.</li>
							<li>Work out what you are going to hear from the vocabulary of the questions and/or pictures.</li>
							<li>Translate any pictures into words and look for details that are different in the options.</li>
							<li>Decide what kind of information you need to listen out for.</li>
						</ul>
					</li>
					<li>
						Answer the IELTS Listening questions as you listen: The information will be given in the same order as the questions, although it might be expressed differently. Be prepared not to hear the first or even second answer choices mentioned.
					</li>
					<li>
						Remember to make notes: Remember to make notes beside the question if you are not sure of the answer, so you can go back later.
					</li>
					<li>Don’t leave any questions unanswered.</li>
					<li>Remember to listen for people correcting themselves: Remember to listen for people correcting themselves when speaking. They may discuss one of the answer choices and then reject it.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Listening Questions - Short Answer </strong>
				<ol class="mt-1" type="a">
					<li>
						Skim the IELTS Listening questions quickly: Skim the questions quickly before the recording starts and during the 30 seconds given. As you do this,
						<ul>
							<li>Underline the key words.</li>
							<li>Try to work out what you are going to hear.</li>
							<li>Decide what you need to listen out for.</li>
							<li>Look out for question words like ‘where’ and ‘who’ which indicate you should listen for specific things like places and people.</li>
						</ul>
					</li>
					<li>Answer the questions as you listen: Answer the questions as you listen. The answers will be given in the same order as the questions although they may be expressed differently.</li>
					<li>Remember to make notes: Make notes beside the question if you are not sure of the answer.</li>
					<li>Number of words: The answer could be one word, two words or three words but not four or more. If you think you need more than three words your answer is probably incorrect.</li>
				</ol>
			</li>
