						<ul>
							<li>Underline the key words.</li>
							<li>Try to work out what you are going to hear.</li>
							<li>Decide what you need to listen out for.</li>
							<li>Anticipate grammatical form as well as vocabulary.</li>
							<li>If the sentences are part of a table, check whether they are horizontal or vertical.</li>
						</ul>
					</li>
					<li>Grammatically correct: Make sure your sentences are grammatically correct.</li>
					<li>Answer the questions as you listen: Answer the questions as you listen. The answers will be given in the same order as the questions although they may be expressed differently. </li>
					<li>Remember to make notes: Make notes beside the question if you are not sure of the answer.</li>
					<li>Number of words: The answer could be one word, two words or three words but not four or more. If you think you need more than three words your answer is probably incorrect.</li>
				</ol>
			</li>
