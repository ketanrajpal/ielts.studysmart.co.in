			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips Of IELTS Writing Test</strong> <br>IELTS writing task is a time bound task and it very important for the student to understand the importance of time management in this module. If each aspect of writing has to be achieved then it is essential to allocate time and write accordingly. 
				<ol class="mt-1" type="a">
					<li>
						Understanding the prompt/statement
						<ul>
							<li>The student should ideally spend a minute for IELTS Writing task 1 and maybe 2 minutes for IELTS Writing task 2 to understand the question. In task 1 academic, the student should absorb the key information of the diagram. For general it is essential to understand the letter type and how to address the same.</li>
							<li>Task 2 needs little more understanding as not only the statement is given but also to indentify the response. This time can also be utilized frame the paraphrase.</li>
						</ul>
					</li>
					<li>
						Outline the task
						<ul>
							<li>After understanding it is important to outline the task. </li>
							<li>For IELTS Writing academic task 1 it is crucial that they encapsulate all the features of the diagram: the key trait and the trend.</li>
							<li>For IELTS Writing general task 1 it required to write down the main ideas that are going to structure the letter.</li>
							<li>For IELTS Writing task 2 this time should be exclusively dedicated in paraphrasing the statement as this is going to form the introduction of the essay. Also the supporting statement of the essay should be formulated as this will give way to the following paragraphs of the essay. </li>
							<li>The time spent on doing this should be approximately 1 minute for task 1 and 2 minutes for task 2.</li>
						</ul>
					</li>
					<li>
						Structuring
						<ul>
							<li>This time is ideally, after one when has understood the task and outlined the task, for jotting down the ideas in an order. Which idea will be the opening and the subsequent ones?</li>
							<li>In task 2 this plays a vital role as an idea can be stronger than the others and the student can elaborate on that particular one while on the others little less elaboration. Here the student then can decide the body of their essay emphasizing the stronger idea as their opening paragraph of the body. They can club the two weaker ideas to form the next paragraph, with correct cohesive device. To show a contrasting idea the candidate can make use of discourse markers in the following paragraph. This shall not hinder the coherence of the essay.</li>
							<li>Ideally task 1  - 2 minutes and task 2 - 3 minutes.</li>
						</ul>
					</li>
					<li>
						Keep the idea and the language simple <br> While writing, it is necessary to remember that IELTS Test is not marking them on the subject knowledge but on the knowledge of English Language. So it the student uses complex ideas or complex words does not mean a higher band score. Yet, if the ideas are simple and are coherent with correct syntax will fetch a higher band score.
					</li>
					<li>
						Focus on the language and not the length <br> Try to keep the idea of the paragraph and the main theme in mind. Do not get deviated while writing. Usually when students try to increase the length of the essay the main idea is defeated and so the task response is affected. Rather than focusing on the length it is important to keep the language and the central idea and the idea of the paragraph as the prime focus. Try to be consistent.
					</li>
					<li>
						Conclusion
						<ul>
							<li>In this paragraph the student should give a positive outlook to the writing in IELTS Writing task 1</li>
							<li>In IELTS Writing task 2 the student should try to balance the views stated. The ideas should be highlighted and connected to give a stable platform.</li>
							<li>The students have approximately 14 minutes to write task 1 and 30 minutes to write task 2.</li>
						</ul>
					</li>
					<li>
						Proof reading
						<ul>
							<li>This is last step in one’s writing and it an indispensible step which is often ignored. While doing so the student should check for grammatical and spelling mistakes. This is not the time to include or exclude the ideas but to check for the cohesive devices and the correct structuring of the essay.</li>
							<li>The candidate should set aside 2 minutes for task 1 and 3 minutes for task 2.  </li>
						</ul>
					</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Tips For IELTS Writing Task 1</strong>
				<ol class="mt-1" type="a">
					<li>Become familiar with the graph types. </li>
					<li>Do lots of practice exercises in describing graphs. </li>
					<li>Practice identifying key information in graphs and charts.	 </li>
					<li>Practice working out what graph axes represent. </li>
					<li>
						Become familiar with a variety of ways to:
						<ul>
							<li>Express percentages</li>
							<li>Express numbers & statistical data</li>
							<li>Express the key points in a graph or describe trends</li>
							<li>Describe increase and decrease</li>
							<li>Make comparisons between data.</li>
						</ul>
						For example, ‘words to describe the degree of change’ – ‘slight’, ‘dramatic’, ‘significant’ & ‘moderate’.
					</li>
					<li>
						Look at the language used for:
						<ul>
							<li>Describing charts (e.g.  Pie charts).</li>
							<li>Describing tables. </li>
							<li>Describing a process. </li>
							<li>Describing how something works.   </li>
							<li>Describing and comparing objects.   </li>
							<li>Writing introductory sentences. </li>
						</ul>
					</li>
					<li>Make sure you know how to use ‘sequencing expressions’ when describing a process.</li>
					<li>Think about the topics that could be used for this task. </li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Letter Writing Tips</strong>
				<ol class="mt-1" type="a">
					<li>Make a Plan</li>
					<li>Use the Correct Style</li>
					<li>Answer the Whole Question</li>
					<li>Boost the Grammar Score With Complex Sentences</li>
					<li>Use the Correct Tense</li>
					<li>Begin and End Your Letter Correctly</li>
					<li>Do not go over the Word Limit</li>
					<li>Follow the 100% Rule</li>
					<li>Don’t Be Overly Informal.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Tips For IELTS Writing Task 2:</strong>
				<ol class="mt-1" type="a">
					<li>Study the question carefully. The instructions will always start with the sentence ‘Write about the following topic…’ and include the sentence beginning ‘Give reasons for your answer…’.</li>
					<li>Underline key points in the question.</li>
					<li>Make sure you read all parts of the question, so you include everything required.</li>
					<li>Quickly brainstorm the topic. Think about your point of view. Think about any personal experience you can add.</li>
					<li>Write a plan. Decide which points are your main points or topic sentences.</li>
					<li>Decide what supporting details you have for each main point. You should have at least two main points.</li>
					<li>Make sure you organise your points logically.</li>
					<li>You will need an introductory statement, body and conclusion.</li>
					<li>Remember to link ideas within/between sentences and paragraphs.</li>
					<li>You will receive a higher mark if your use a range of structures and vocabulary.</li>
					<li>Don’t copy from the question as this will not be assessed. Use your own words.</li>
					<li>Don’t take more than 40 minutes over this task.</li>
					<li>Leave at least 5 minutes to check through what you have written.</li>
					<li>You will lose marks if your essay is less than the required number of words.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips For IELTS Writing Before The Exam</strong>
				<ol class="mt-1" type="a">
					<li>Practice writing answers to the tasks in the time allowed, especially if you haven’t written at speed by hand for a while.</li>
					<li>Keep up-to-date with current affairs.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips For IELTS Writing During The Exam</strong>
				<ol class="mt-1" type="a">
					<li>Use a pen. It should be easier to write faster with a pen.</li>
					<li>Make sure you have a checklist in mind for editing your writing at the end. </li>
					<li>Make one up now with the errors you know you make. </li>
				</ol>
			</li>
		</ol>
