			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Multiple Choice </strong>
				<ol class="mt-1" type="a">
					<li>Read the instructions carefully: Read the instructions carefully and check how many letters you need to underline.</li>
					<li>
						Skim all the  IELTS Reading questions and the  answer  choices. Skim all the questions and the answer choices quickly. As you do this,
						<ul>
							<li>Underline the keywords (the words that give you the most information).</li>
							<li>Try to get an idea of the topic you will be reading about from the vocabulary of the questions.</li>
							<li>Look at any illustrations or diagrams that go with the text.</li>
							<li>Go back to the first question. Decide if you are looking for specific information or whether the question requires you to understand the whole text. Then either scan or skim the text, as appropriate, to find the answer.</li>
						</ul>
					</li>
					<li>Read the relevant part of the text very carefully.</li>
					<li>Don't leave any questions unanswered.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question  - Short Answer </strong>
				<ol class="mt-1" type="a">
					<li>Read the instructions carefully:</li>
					<li>
						Skim all the questions quickly. As you do this:
						<ul>
							<li>Underline the key words.</li>
							<li>Decide what information you need to find in the text.</li>
							<li>Look out for question words like ‘where’ and ‘who’ which indicate you should listen for     specific things like places and people.</li>
						</ul>
					</li>
					<li>Go back to the first question and decide what part of the text you need to read.</li>
					<li>Read the part carefully to find the answer.</li>
					<li>You don’t have to write a complete sentence but it does have to be grammatically correct.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Sentence Completion </strong>
				<ol class="mt-1" type="a">
					<li>
						Quickly read through all the sentences halves: As you do this
						<ul>
							<li>Underline the key words.</li>
							<li>Try to work out what information you need.</li>
							<li>Think about the grammatical form as well as the vocabulary that should follow immediately from the stem.</li>
						</ul>
					</li>
					<li>Go back to the first sentence and decide what information you need to complete it.</li>
					<li>Find the place where the information should be in the text and read it carefully.</li>
					<li>Look out for synonyms and parallel expressions: Look out for synonyms and parallel expressions because the questions are not likely to use the same words as those in the text.</li>
					<li>Make sure your sentences make sense both logically and grammatically.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question – Notes / Table / Form / Summary / Flow Chart / Diagram Completion Questions </strong>
				<ol class="mt-1" type="a">
					<li>
						Look at the table or form Carefully:
						<ul>
							<li>Examine any headings or subheadings.</li>
							<li>Try to get an idea of the topic.</li>
							<li>Decide what section of the passage the exercise covers.</li>
							<li>Anticipate grammatical form as well as vocabulary.</li>
							<li>If a box of answers is given, see if you can guess any of the matches & eliminate unlikely answers.</li>
							<li>if the question is in the form  of  a table, work out which way it is best to read it horizontally or vertically.</li>
							<li>If the question is in the form of a summary, read through it first and see if you can guess any of the missing words.</li>
						</ul>
					</li>
					<li>
						Alternative rubrics : There may be alternative rubrics for these completion questions e.g. ‘complete the notice’ or ‘complete the explanation’ or ‘complete the news report’.
					</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Yes, No, Not Given Or True, False, Not Given</strong>
				<ol class="mt-1" type="a">
					<li>Quickly read through all the statements to get an idea about the topic.</li>
					<li>Read the first statement more carefully.	</li>
					<li>Underline the key words so you understand the main point.</li>
					<li>Search for the section of the text which deals with the idea or fact.</li>
					<li>Read Carefully:</li>
					<li>Once you have found the relevant section, read it carefully. For type 1,  if  the  statement  disagrees  with  the  writer’s  opinion,  then  select  ‘no’ and if  the author  doesn’t  give an opinion, select ‘not given’.  For  type 2,  if the statement is the opposite to  the  information  in  the  text,  then  select ‘false’ and if there is no mention of it, select ‘not given’.</li>
					<li>Continue with the rest of the statements.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Classification Questions</strong>
				<ol class="mt-1" type="a">
					<li>Make sure you know how many classifications there are and what letters you have to use.</li>
					<li>Read the classifications carefully and make sure you don’t confuse the letters which represent each one.</li>
					<li>Read the statements/phrases or words beside the	question numbers and underline key words.</li>
					<li>Start with the first statement and work your way through them one by one, searching the text to find where the information is mentioned.</li>
					<li>The questions will not necessarily be in the same order as the text and the wording will probably be different in the text so look out for synonyms and parallel expressions.</li>
					<li>When you’ve located the reference in the text, read it carefully and select your answer.	</li>
					<li>Don’t leave any statements without a letter.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For Matching:</strong>
				<ol class="mt-1" type="a">
					<li>Take the names of the sources one by one and find them in the text and underline them.</li>
					<li>When you have located a name, read carefully to see what is said about his/her opinions.</li>
					<li>Look at the list of opinions and see if you can make a match.</li>
					<li>Remember that the text is not likely to use the same words as the</li>
					<li>Questions, so look for synonyms and parallel expressions.</li>
					<li>Also, be aware that the sources may be referred to in more than one place in the text.</li>
					<li>The opinions in the task are not listed in the same order as they appear in the text.</li>
					<li>Phrases like ‘he said’ or ‘in his opinion’ should help you locate the arguments.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Choosing Headings</strong>
				<ol class="mt-1" type="a">
					<li>Make sure you know which paragraphs or sections you have to sum up.</li>
					<li>Read the first paragraph or section and try to sum up, in your own words, what it is about.</li>
					<li>Then search through the bank of headings for the best answer.</li>
					<li>Make sure the heading you have chosen sums up the entire paragraph and not just one idea within it.</li>
					<li>If you have to sum up the entire text. Read the whole text before looking at the bank of headings.  Try to think of  your  own heading and then look at the options.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>Preparation Strategy For IELTS Reading Question - Labelling A Diagram Which Has Numbered Parts</strong>
				<ol class="mt-1" type="a">
					<li>Study the diagram and the labels if they are given.</li>
					<li>See if you can guess any of the answers.</li>
					<li>The information will be given in the same order as the numbers on the diagram.</li>
					<li>Scan the text  to  find the  information. If labels are not provided, make sure you use words from the text.</li>
				</ol>
			</li>
			<li class="font-family-lato mt-1 mb-2 pb-2">
				<strong>General Tips For IELTS Reading Test </strong>
				<ol class="mt-1" type="a">
					<li>
						Before the Exam
						<ul>
							<li>Make sure you are familiar with the instructions for the different question types so you can quickly glance at the questions and know what to do.</li>
							<li>Read as much as possible.</li>
							<li>Work on your reading skills such as ‘guessing the meanings of unknown words’, ‘understanding reference words in texts’ and reading quickly.</li>
							<li>
								Read newspaper articles and practice the following:
								<ul>
									<li>Dividing the content into facts and opinions</li>
									<li>Finding the topic sentences of paragraphs</li>
									<li>Writing summaries</li>
									<li>Interpreting any diagrams or tables</li>
									<li>Thinking of headings you could give to paragraphs</li>
									<li>Underlining the pronouns and working out what they refer to</li>
									<li>Underlining unknown words and seeing if you can work out what they mean</li>
									<li>Reading the first paragraph and seeing if you can predict what will come next</li>
								</ul>
							</li>
							<li>Work on expanding your vocabulary. </li>
							<li>Do as many practice tests as you can to get used to the rubric and the task types.</li>
						</ul>
					</li>
					<li>
						During the Exam
						<ul>
							<li>Look through the whole reading module first.</li>
							<li>
								Quickly look at the texts. Study any:
								<ul>
									<li>Titles</li>
									<li>headings</li>
									<li>sub headings</li>
									<li>illustrations</li>
									<li>diagrams</li>
									<li>words in bold type or italics</li>
								</ul>
							</li>
							<li>Read the questions carefully. Identify the question type. Make sure you do exactly what they say.</li>
							<li>Read the glossary beside the passage, if there is one.</li>
							<li>Don’t spend too long on one question.</li>
							<li>If you don’t know the answer, guess. Incorrect answers are not penalized.</li>
							<li>Make sure you copy all words taken from the texts correctly as incorrect spelling is penalized.</li>
							<li>Don’t forget your answer may be given in a diagram, graph or illustration.</li>
						</ul>
					</li>
				</ol>
			</li>
		</ol>
